using System.Numerics;
using Content.Shared.Sectors.Prototypes;
using Content.Shared.Shuttles.BUIStates;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Map;
using Robust.Shared.Physics.Components;
using Content.Shared.Shuttles.Components;
using Content.Shared.Sectors;
using Robust.Shared.Prototypes;

namespace Content.Client.Shuttles.UI;

[GenerateTypedNameReferences]
public sealed partial class NavScreen : BoxContainer
{
    [Dependency] private readonly IEntityManager _entManager = default!;
    [Dependency] private readonly IPrototypeManager _prototypes = default!;
    private SharedTransformSystem _xformSystem;
    private SharedSectorSystem _sectorSystem;

    private EntityUid? _consoleEntity; // Entity of controlling console
    private EntityUid? _shuttleEntity;
    private Dictionary<SpaceSector, string> _sectorHazards = new();

    public Action<ShuttleDampingMode>? OnDampingModeChanged;

    public NavScreen()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        _xformSystem = _entManager.System<SharedTransformSystem>();
        _sectorSystem = _entManager.System<SharedSectorSystem>();

        IFFToggle.OnToggled += OnIFFTogglePressed;
        IFFToggle.Pressed = NavRadar.ShowIFF;

        DockToggle.OnToggled += OnDockTogglePressed;
        DockToggle.Pressed = NavRadar.ShowDocks;

        DampingModeButton.AddItem(Loc.GetString("shuttle-console-damping-cruise"), (int)ShuttleDampingMode.Cruise);
        DampingModeButton.AddItem(Loc.GetString("shuttle-console-damping-normal"), (int)ShuttleDampingMode.Normal);
        DampingModeButton.AddItem(Loc.GetString("shuttle-console-damping-anchor"), (int)ShuttleDampingMode.Anchor);
        DampingModeButton.OnItemSelected += OnDampingSelected;

        SortModeButton.AddItem(Loc.GetString("shuttle-console-sort-none"), (int)IFFSortMode.None);
        SortModeButton.AddItem(Loc.GetString("shuttle-console-sort-station"), (int)IFFSortMode.Station);
        SortModeButton.AddItem(Loc.GetString("shuttle-console-sort-ship"), (int)IFFSortMode.Ship);
        SortModeButton.OnItemSelected += OnSortSelected;
        SortModeButton.SelectId((int)IFFSortMode.None);
    }

    public void SetShuttle(EntityUid? shuttle)
    {
        _shuttleEntity = shuttle;
    }

    public void SetConsole(EntityUid? console)
    {
        _consoleEntity = console;
        NavRadar.SetConsole(console);
    }

    private void OnIFFTogglePressed(BaseButton.ButtonEventArgs args)
    {
        NavRadar.ShowIFF ^= true;
        args.Button.Pressed = NavRadar.ShowIFF;
    }

    private void OnDockTogglePressed(BaseButton.ButtonEventArgs args)
    {
        NavRadar.ShowDocks ^= true;
        args.Button.Pressed = NavRadar.ShowDocks;
    }

    public void UpdateState(NavInterfaceState scc)
    {
        NavRadar.UpdateState(scc);
        DampingModeButton.SelectId((int)scc.DampingMode);
        _sectorHazards = scc.SectorWeatherEvents;
    }

    private string ResolveHazardName(string prototypeId)
    {
        if (_prototypes.TryIndex<SectorWeatherPrototype>(prototypeId, out var weather) &&
            !string.IsNullOrWhiteSpace(weather.Name))
        {
            return Loc.GetString(weather.Name);
        }

        return prototypeId;
    }

    public void SetMatrix(EntityCoordinates? coordinates, Angle? angle)
    {
        _shuttleEntity = coordinates?.EntityId;
        NavRadar.SetMatrix(coordinates, angle);
    }

    protected override void Draw(DrawingHandleScreen handle)
    {
        base.Draw(handle);

        if (!_entManager.TryGetComponent(_shuttleEntity, out TransformComponent? gridXform) ||
            !_entManager.TryGetComponent(_shuttleEntity, out PhysicsComponent? gridBody))
        {
            return;
        }

        var (_, worldRot, worldMatrix) = _xformSystem.GetWorldPositionRotationMatrix(gridXform);
        var worldPos = Vector2.Transform(gridBody.LocalCenter, worldMatrix);

        // Get the positive reduced angle.
        var displayRot = -worldRot.Reduced();

        GridPosition.Text = Loc.GetString("shuttle-console-position-value",
            ("X", $"{worldPos.X:0.0}"),
            ("Y", $"{worldPos.Y:0.0}"));
        var currentSector = _sectorSystem.GetSector(worldPos);
        GridSector.Text = Loc.GetString("shuttle-console-sector-value",
            ("sector", _sectorSystem.GetSectorName(currentSector)));

        if (_sectorHazards.TryGetValue(currentSector, out var hazardId))
        {
            GridHazardWeather.Text = Loc.GetString("shuttle-console-hazard-event-single", ("event", ResolveHazardName(hazardId)));
            GridHazardWeather.Modulate = Color.OrangeRed;
        }
        else
        {
            GridHazardWeather.Text = Loc.GetString("shuttle-console-hazard-event-none");
            GridHazardWeather.Modulate = Color.White;
        }

        GridOrientation.Text = Loc.GetString("shuttle-console-orientation-value",
            ("angle", $"{displayRot.Degrees:0.0}"));

        var gridVelocity = gridBody.LinearVelocity;
        gridVelocity = displayRot.RotateVec(gridVelocity);
        // Get linear velocity relative to the console entity
        GridLinearVelocity.Text = Loc.GetString("shuttle-console-linear-velocity-value",
            ("X", $"{gridVelocity.X + 10f * float.Epsilon:0.0}"),
            ("Y", $"{gridVelocity.Y + 10f * float.Epsilon:0.0}"));
        GridAngularVelocity.Text = Loc.GetString("shuttle-console-angular-velocity-value",
            ("angularVelocity", $"{-MathHelper.RadiansToDegrees(gridBody.AngularVelocity) + 10f * float.Epsilon:0.0}"));
    }

    private void OnDampingSelected(OptionButton.ItemSelectedEventArgs args)
    {
        DampingModeButton.SelectId(args.Id);
        OnDampingModeChanged?.Invoke((ShuttleDampingMode)args.Id);
    }

    private void OnSortSelected(OptionButton.ItemSelectedEventArgs args)
    {
        SortModeButton.SelectId(args.Id);
        NavRadar.SortMode = (IFFSortMode)args.Id;
    }
}
